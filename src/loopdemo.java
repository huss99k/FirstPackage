public class loopdemo {

    public static void main(String[] args) {

        System.out.println("Talk is cheap. show me the code");
        // loop is a construct that enables for that prevent repeated codes
        //While,Do-While,For

        int count = 0;
        if(count < 100){
            System.out.println("Talk is cheap. show ,e the code");
        }

        while(count < 100) { //variable that tracks the amount of iteration
            System.out.println("Talk is cheap. show ,e the code");//loop condition
            count++; // update statement

        }
    }
}
